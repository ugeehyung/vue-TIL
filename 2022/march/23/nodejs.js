// Runtime(런타임)이란?
// Runtime: 프로그램이 실행되고 있는 곳. 프로그래밍 언어가 구동되는 환경.
// JavaScript 에 Web Browser에서 작동하는 Javascript + Node.js 에서 작동하는 Javascript 가 존재.
// 여기서 Web Browser, Node js가 런타임.

// Node.js 란?
// Node.js®: Chrome V8 JavaScript 엔진으로 빌드된 JavaScript runtime.
// Javascript 기반으로 구성된 서버 사이드 서비스를 Javascript로 구현할 수 있게 만든 런타임.
// 이벤트 기반, Non 블로킹 I/O 모델을 사용해 가볍고 효율적이다.

// npm 이란?
// Node.js의 패키지 생태계
// npm은 세계에서 가장 큰 오픈 소스 라이브러리이다.